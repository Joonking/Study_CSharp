using System;

namespace Study_CSharp._04_데이터를가공하는연산자
{
    internal class _04_RelationalOperator
    {
        static void Main(string[] args)
        {
            Console.WriteLine($"3 > 4  : {3 > 4}");
            Console.WriteLine($"3 >= 4 : {3 >= 4}");
            Console.WriteLine($"3 < 4  : {3 < 4}");
            Console.WriteLine($"3 <= 4 : {3 <= 4}");
            Console.WriteLine($"3 == 4 : {3 == 4}");
            Console.WriteLine($"3 != 4 : {3 != 4}");
        }
    }
}
